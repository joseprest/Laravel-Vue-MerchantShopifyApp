    /**
     * Handle the user "created" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function created(User $user)
    {
        try {
            if(!Auth::user() || $user->email == Auth::user()->email) {
                Mail::to($user->email)->send(new WelcomeEmail($user));
            }
        } catch (\Exception $e) {
            Log::error('Cannot send welcome email: '.$e->getMessage());
        }
    }

